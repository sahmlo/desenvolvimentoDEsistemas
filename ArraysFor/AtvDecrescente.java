// Atividade 2 - Contagem Decrescente
public class AtvDecrescente {
    public static void main(String[] args) {
        System.out.print("Contagem decrescente começando em 20 até 10: ");

        for (int i = 20; i >= 10; i--) {
            System.out.println(i);
        }
        System.out.println("Contagem fiunalizada!");
    }
}
