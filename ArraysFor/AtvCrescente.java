// Atividade 1 - Contagem Crescente

public class AtvCrescente {
    public static void main(String[] args) {
        System.out.print("Contagem crescente começando em 10 até 20: ");

        for (int i = 10; i <= 20; i++) {
            System.out.println(i);
        }
        System.out.println("Contagem fiunalizada!");
    }
}
