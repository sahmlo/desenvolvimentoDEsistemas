public class Variavel {
    public static void main(String[] args) {
        // DECLARAÇÃO DE VARIÁVEIS
        String nome = "Amanda"; // VARIÁVEL STRING
        int idade = 22; // VARIÁVEL INTEIRA
        char genero = 'F'; // VARIÁVEL CARACTERE
        float altura = 1.73f; // VARIÁVEL DE PONTO FLUTUANTE TEM LIMITE DE PRECISÃO
        double peso = 50.73; // VARIÁVEL DE PONTO FLUTUANTE COM MAIOR PRECISÃO

        // IMPRESSÃO DAS VARIÁVEIS
        System.out.println("Olá meu nome é " + nome + " tenho " + idade + " anos, sou do gênero " + genero + ", minha altura é " + altura + "m e meu peso é " + peso + "kg.");
    }
}